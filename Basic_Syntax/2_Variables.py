# Variáveis em Python
# Variável é um espaço na memória que armazena um valor, e esse valor pode ser alterado durante a execução do programa.
# Variáveis são criadas quando você atribui um valor a elas.
# Variáveis são sensíveis a maiúsculas e minúsculas, ou seja, nome e Nome são variáveis diferentes.
# Variáveis podem ser criadas com letras, números e o caractere de sublinhado (_), mas não podem começar com números.
# Nomes de variáveis não podem conter espaços ou caracteres especiais, como @, #, $, %, etc.

# Criando variáveis
nome = "Lucas"
idade = 20
email = "teste@teste.com"
in_online = True
price = 10.99

print(nome, idade, email, in_online , price)


